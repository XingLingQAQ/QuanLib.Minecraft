using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace QuanLib.Minecraft.Block
{
    public abstract class ColorBlock
    {
        /// <summary>
        /// 白色
        /// </summary>
        public abstract string White { get; }

        /// <summary>
        /// 橙色
        /// </summary>
        public abstract string Orange { get; }

        /// <summary>
        /// 品红色
        /// </summary>
        public abstract string Magenta { get; }

        /// <summary>
        /// 淡蓝色
        /// </summary>
        public abstract string LightBlue { get; }

        /// <summary>
        /// 黄色
        /// </summary>
        public abstract string Yellow { get; }

        /// <summary>
        /// 黄绿色
        /// </summary>
        public abstract string Lime { get; }

        /// <summary>
        /// 粉红色
        /// </summary>
        public abstract string Pink { get; }

        /// <summary>
        /// 灰色
        /// </summary>
        public abstract string Gray { get; }

        /// <summary>
        /// 淡灰色
        /// </summary>
        public abstract string LightGray { get; }

        /// <summary>
        /// 青色
        /// </summary>
        public abstract string Cyan { get; }

        /// <summary>
        /// 紫色
        /// </summary>
        public abstract string Purple { get; }

        /// <summary>
        /// 蓝色
        /// </summary>
        public abstract string Blue { get; }

        /// <summary>
        /// 棕色
        /// </summary>
        public abstract string Brown { get; }

        /// <summary>
        /// 绿色
        /// </summary>
        public abstract string Green { get; }

        /// <summary>
        /// 红色
        /// </summary>
        public abstract string Red { get; }

        /// <summary>
        /// 黑色
        /// </summary>
        public abstract string Black { get; }
    }

}
